# There are three types of quoutes in python

# Single and double quotes
print("First text - 'text'")

# Triple quotes
print("""This is very long text
which can span along multiple lines""")

print("New line can be also used in other way \nnext line")

# Strings can be joined in this way
text = "First" + " " + "second"
print(text)

# Joining strings using print function
print("First", "second")

# Or using format specified
print("%s %s" % ('First', 'Second')) # this construct is known as tuple and it's immutable

